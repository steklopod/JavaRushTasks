package com.javarush.task.task05.task0526;

/* 
Мужчина и женщина
*/

public class Solution {
    public static void main(String[] args) {
        Man man1 = new Man("Vasa", 16, "Africa");   //тут было сказано создать объекты
        Man man2 = new Man("Misha", 19, "Alaska");
        Woman woman1 = new Woman("Kisa", 18, "Spain");
        Woman woman2 = new Woman("Lapa", 19, "France");
        System.out.println(man1.name + " " + man1.age + " " + man1.address);   //тут вывести их
        System.out.println(man2.name + " " + man2.age + " " + man2.address);
        System.out.println(woman1.name + " " + woman1.age + " " + woman1.address);
        System.out.println(woman2.name + " " + woman2.age + " " + woman2.address);
    }

    public static class Man {
        String name;
        int age;
        String address;

        public Man(String name, int age, String address) {
            this.name = name;
            this.age = age;
            this.address = address;
        }
    }

    public static class Woman {
        String name;
        int age;
        String address;

        public Woman(String name, int age, String address) {
            this.name = name;
            this.age = age;
            this.address = address;
        }
    }
}
